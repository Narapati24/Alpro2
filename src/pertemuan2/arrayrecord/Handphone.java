package pertemuan2.arrayrecord;

public class Handphone {

	private String sId, sMerk, sJenis;
	private int iHarga;
	
	public String getsId() {
		return sId;
	}
	public void setsId(String sId) {
		this.sId = sId;
	}
	public String getsMerk() {
		return sMerk;
	}
	public void setsMerk(String sMerk) {
		this.sMerk = sMerk;
	}
	public String getsJenis() {
		return sJenis;
	}
	public void setsJenis(String sJenis) {
		this.sJenis = sJenis;
	}
	public int getiHarga() {
		return iHarga;
	}
	public void setiHarga(int iHarga) {
		this.iHarga = iHarga;
	}
	
	
}
